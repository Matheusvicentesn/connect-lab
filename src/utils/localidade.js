export function cordenadas(estado, setLat, setLong) {
  if (estado === "Acre") {
    setLat("-8.770");
    setLong("-70.550");
  } else if (estado === "Alagoas") {
    setLat("-9.417");
    setLong("-36.617");
  } else if (estado === "Amapá") {
    setLat("-3.070");
    setLong("-61.660");
  } else if (estado === "Amazonas") {
    setLat("10.435");
    setLong("-73.249");
  } else if (estado === "Bahia") {
    setLat("-12.960");
    setLong("-38.510");
  } else if (estado === "Ceará") {
    setLat("-3.710");
    setLong("-38.540");
  } else if (estado === "Distrito Federal") {
    setLat("-15.830");
    setLong("-47.860");
  } else if (estado === "Santa Catarina") {
    setLat("-27.330");
    setLong("-49.440");
  } else {
    setLat("-23.55");
    setLong("-46.64");
  }
}
